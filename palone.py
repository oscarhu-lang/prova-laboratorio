class Player:
    def __init__(self, start_x, start_y):
        self.grid_x = start_x
        self.grid_y = start_y
        self.pixel_x = start_x * TILE_SIZE
        self.pixel_y = start_y * TILE_SIZE
        self.target_px = self.pixel_x
        self.target_py = self.pixel_y
        
        self.is_moving = False
        self.dir = 'down'
        self.anim_idx = 0.0

    def move(self, dx, dy, direction):
        if self.is_moving:
            return
        self.dir = direction
        nx, ny = self.grid_x + dx, self.grid_y + dy
        
        if 0 <= ny < len(game_map) and 0 <= nx < len(game_map[0]):
            if game_map[ny][nx] != 1:
                self.grid_x, self.grid_y = nx, ny
                self.target_px, self.target_py = nx * TILE_SIZE, ny * TILE_SIZE
                self.is_moving = True

    def update(self):
        if self.is_moving:
            if self.pixel_x < self.target_px:
                self.pixel_x += MOVE_SPEED
            elif self.pixel_x > self.target_px:
                self.pixel_x -= MOVE_SPEED
            if self.pixel_y < self.target_py:
                self.pixel_y += MOVE_SPEED
            elif self.pixel_y > self.target_py:
                self.pixel_y -= MOVE_SPEED
            
            if self.pixel_x == self.target_px and self.pixel_y == self.target_py:
                self.is_moving = False
            self.anim_idx += 0.2
        else:
            self.anim_idx += 0.02
            
        if self.anim_idx >= 8:
            self.anim_idx = 0

class GameWindow(arcade.Window):
    def __init__(self):
        super().__init__(SCREEN_WIDTH, SCREEN_HEIGHT, SCREEN_TITLE)
        arcade.set_background_color(arcade.color.DARK_GRAY)
        
        self.tile_texture = None
        self.goal_texture = None
        self.animations = {}
        self.player = None
        self.view_left = 0
        self.view_bottom = 0

    def setup(self):
        try:
            self.tile_texture = arcade.load_texture("foto/unnamed.jpg")
        except:
            self.tile_texture = arcade.make_soft_square_texture(TILE_SIZE, arcade.color.GRAY, 255, 255)
        
        self.goal_texture = arcade.make_soft_square_texture(TILE_SIZE, arcade.color.TURQUOISE, 255, 255)
        
        self.animations = {
            'run_up': load_animation('foto/run_up.png'),
            'run_down': load_animation('foto/run_down.png'),
            'run_left': load_animation('foto/run_left.png'),
            'run_right': load_animation('foto/run_right.png'),
            'idle_down': load_animation('foto/idle_down.png')
        }
        
        self.player = Player(13, 3)

    def on_key_press(self, key, modifiers):
        if not self.player.is_moving:
            if key == arcade.key.W:
                self.player.move(0, -1, 'up')
            elif key == arcade.key.S:
                self.player.move(0, 1, 'down')
            elif key == arcade.key.A:
                self.player.move(-1, 0, 'left')
            elif key == arcade.key.D:
                self.player.move(1, 0, 'right')

    def on_update(self, delta_time):
        self.player.update()
        player_world_x = self.player.pixel_x + TILE_SIZE // 2
        map_height_px = len(game_map) * TILE_SIZE
        player_world_y = map_height_px - self.player.pixel_y - TILE_SIZE // 2
        
        
        changed = False
        
        left_boundary = self.view_left + SCREEN_WIDTH // 2
        if player_world_x < left_boundary - 10 or player_world_x > left_boundary + 10:
            self.view_left = player_world_x - SCREEN_WIDTH // 2
            changed = True
            
        bottom_boundary = self.view_bottom + SCREEN_HEIGHT // 2
        if player_world_y < bottom_boundary - 10 or player_world_y > bottom_boundary + 10:
            self.view_bottom = player_world_y - SCREEN_HEIGHT // 2
            changed = True
            
        
        map_width_px = len(game_map[0]) * TILE_SIZE
        if self.view_left < 0:
            self.view_left = 0
            changed = True
        if self.view_left > map_width_px - SCREEN_WIDTH:
            self.view_left = map_width_px - SCREEN_WIDTH
            changed = True
            
        if self.view_bottom < 0:
            self.view_bottom = 0
            changed = True
        if self.view_bottom > map_height_px - SCREEN_HEIGHT:
            self.view_bottom = map_height_px - SCREEN_HEIGHT
            changed = True

        if changed:
            arcade.set_viewport(self.view_left, 
                                SCREEN_WIDTH + self.view_left, 
                                self.view_bottom, 
                                SCREEN_HEIGHT + self.view_bottom)

    def on_draw(self):
        arcade.start_render()
        
        map_height_px = len(game_map) * TILE_SIZE
        
        
        for r_idx, row in enumerate(game_map):
            for c_idx, cell in enumerate(row):
                world_x = c_idx * TILE_SIZE + TILE_SIZE // 2
                world_y = map_height_px - (r_idx * TILE_SIZE) - TILE_SIZE // 2
                
                if (self.view_left - TILE_SIZE < world_x < self.view_left + SCREEN_WIDTH + TILE_SIZE and
                    self.view_bottom - TILE_SIZE < world_y < self.view_bottom + SCREEN_HEIGHT + TILE_SIZE):
                    if cell == 1:
                        arcade.draw_texture_rectangle(world_x, world_y, TILE_SIZE, TILE_SIZE, self.tile_texture)
                    elif cell == 2:
                        arcade.draw_texture_rectangle(world_x, world_y, TILE_SIZE, TILE_SIZE, self.goal_texture)

        state = f"run_{self.player.dir}" if self.player.is_moving else "idle_down"
        texture = self.animations[state][int(self.player.anim_idx)]
        
        player_draw_x = self.player.pixel_x + TILE_SIZE // 2
        player_draw_y = map_height_px - self.player.pixel_y - TILE_SIZE // 2
        
        arcade.draw_texture_rectangle(player_draw_x, player_draw_y, TILE_SIZE, TILE_SIZE, texture)

def main():
    window = GameWindow()
    window.setup()
    arcade.run()

if __name__ == "__main__":
    main()