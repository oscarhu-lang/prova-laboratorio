def load_animation(filename, frames_count=8):
    """ Arcade"""
    try:
        frames = []
        full_texture = arcade.load_texture(filename)
        frame_width = full_texture.width // frames_count
        frame_height = full_texture.height
        
        for i in range(frames_count):
            texture = arcade.load_texture(
                filename,
                x=i * frame_width,
                y=0,
                width=frame_width,
                height=frame_height
            )
            frames.append(texture)
        return frames
    except Exception as e:
        print(f"sbaglio {filename}: {e}")
        placeholder = arcade.make_soft_square_texture(TILE_SIZE, arcade.color.GRAY, 255, 255)
        return [placeholder] * frames_count

