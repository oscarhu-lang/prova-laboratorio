def draw_map(data, cam_x, cam_y):
    """1"""
    for row_index, row in enumerate(data):
        for col_index, cell in enumerate(row):
            if cell == 1:
                
                x = col_index * TILE_SIZE - cam_x
                y = row_index * TILE_SIZE - cam_y
                screen.blit(tile_image, (x, y))

clock = pygame.time.Clock()
running = True
while running:
    clock.tick(60) 

    for event in pygame.event.get():
        if event.type == pygame.QUIT:
            running = False

    keys = pygame.key.get_pressed()
    if keys[pygame.K_w]: camera_y -= MOVE_SPEED
    if keys[pygame.K_s]: camera_y += MOVE_SPEED
    if keys[pygame.K_a]: camera_x -= MOVE_SPEED
    if keys[pygame.K_d]: camera_x += MOVE_SPEED

    screen.fill((30, 30, 30)) 
    
    draw_map(game_map, camera_x, camera_y) 
    
    pygame.display.flip()

pygame.quit()